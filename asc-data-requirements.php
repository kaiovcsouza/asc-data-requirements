/**
 * Add rewrite rule for page.
 */
function ascds_rewrite_multilanguage() {
	global $wpdb;

	$sa_origin_path = '';

	$sa_page_id = wp_cache_get( 'sa_page_id' );

	if ( false === $sa_page_id ) {

		// phpcs:ignore WordPress.DB.DirectDatabaseQuery.DirectQuery -- much faster than get_page_by_path() or get_posts().
		$sa_page_id = $wpdb->get_var( "SELECT ID FROM {$wpdb->prefix}posts WHERE post_name='self-assessment'" );

		if ( isset( $sa_page_id ) ) {
			$sa_origin_path = get_permalink( $sa_page_id );
			$slug_path      = trim( str_replace( home_url(), '', get_permalink( $sa_page_id ) ), '/' );
			add_rewrite_rule( '^' . $slug_path . '/([^/]*)/?([^/]*)/?', 'index.php?page_id=' . $sa_page_id . '&satoken=$matches[1]&stage=$matches[2]', 'top' );

			wp_cache_set( 'sa_page_id', $sa_page_id, '', 3600 );
		}
	}

	flush_rewrite_rules( false );

	return array(
		'saOrigin' => $sa_origin_path,
	);
}

