/**
 * Run the ajax process in class-fetchindicators.php with nonce security.
 */
function ascds_ajax_process() {
	check_ajax_referer( 'ascds_nonce' );

	if ( ! defined( 'ASC_MONGO_APIKEY' ) ) {
		echo 'Please define ASC_MONGO_APIKEY in wp-config.php';
		die();
	}

	include_once plugin_dir_path( __FILE__ ) . 'class-fetchrequireddata.php';

	if ( isset( $_SERVER['REQUEST_METHOD'] ) ) {
		if ( 'POST' === $_SERVER['REQUEST_METHOD'] ) {
			$fetchdata = new FetchRequiredData();

			// phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- This is JSON data as received from the API. We should not escape it.
			echo wp_json_encode( $fetchdata->api_call( ASC_MONGO_APIKEY ) );
		} else {
			header( 'Location:' . home_url() );
			die();
		}
	}
	die();
}

